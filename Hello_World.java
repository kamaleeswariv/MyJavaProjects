
public class Hello_World {

	public static void main(String[] args) {
		boolean ioT = true;
		System.out.println(ioT);
		byte range;
		range = 127;
		System.out.println("range of byte :"+range);
		short range1;
		range1 = -127;
		System.out.println(range1);
		
		int range2 = 300000000;
		System.out.println("range of short: "+range2);

		long range3 = 30000000000000000L;
		System.out.println("range of long : "+range3);

		double number = 99.9;
		System.out.println("double:"+number);

		float number1 = 99.9f;
		System.out.println("float: "+number1);

		char letter = '\u0041';
		System.out.println( "char:"+letter);

		char letter2 = 65;
		System.out.println(letter2);

		char letter3 = 'a';
		System.out.println("char: "+letter3);

	}

}
